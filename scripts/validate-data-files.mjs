/**
 * Pre-build data validation gate.
 * Must pass before `next build` is allowed to run.
 * Exit 1 if any required file is missing, empty, or malformed.
 */
import { readFileSync, existsSync } from 'node:fs'
import { join } from 'node:path'

const dataDir = join(process.cwd(), 'public', 'data')

const requiredFiles = [
  {
    path: 'herbs.json',
    minLength: 200,
    description: 'herb profiles',
    source: 'scripts/build-runtime-data.mjs',
  },
  {
    path: 'compounds.json',
    minLength: 100,
    description: 'compound profiles',
    source: 'scripts/build-runtime-data.mjs',
  },
  {
    path: 'summary-indexes/herbs-summary.json',
    minLength: 50,
    description: 'herb summary index',
    source: 'scripts/build-runtime-data.mjs',
  },
  {
    path: 'summary-indexes/compounds-summary.json',
    minLength: 50,
    description: 'compound summary index',
    source: 'scripts/build-runtime-data.mjs',
  },
]

let failures = 0

for (const { path: relPath, minLength, description, source } of requiredFiles) {
  const fullPath = join(dataDir, relPath)

  if (!existsSync(fullPath)) {
    console.error(`[validate-data-files] MISSING: ${relPath} (${description}) — generated by ${source}`)
    failures++
    continue
  }

  let parsed
  try {
    const raw = readFileSync(fullPath, 'utf8')
    parsed = JSON.parse(raw)
  } catch (err) {
    console.error(`[validate-data-files] INVALID JSON: ${relPath} — ${err.message}`)
    failures++
    continue
  }

  if (!Array.isArray(parsed)) {
    console.error(`[validate-data-files] NOT AN ARRAY: ${relPath} — expected array, got ${typeof parsed}`)
    failures++
    continue
  }

  if (parsed.length < minLength) {
    console.error(
      `[validate-data-files] TOO SHORT: ${relPath} — expected ≥${minLength} entries, got ${parsed.length} (generated by ${source})`
    )
    failures++
    continue
  }

  console.log(`[validate-data-files] OK: ${relPath} — ${parsed.length} ${description}`)
}

if (failures > 0) {
  console.error(`\n[validate-data-files] FAILED: ${failures} file(s) did not pass validation. Build aborted.`)
  process.exit(1)
} else {
  console.log(`\n[validate-data-files] All data files valid. Proceeding to next build.`)
}
