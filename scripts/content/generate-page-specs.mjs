#!/usr/bin/env node
/**
 * Page-spec generator — the "assembly line" press.
 *
 * Reads the structured Top-25 data in `page-specs.data.mjs` and emits one
 * specification file per page under `docs/page-specs/<slug>.md`, plus an index.
 *
 * Each spec is a *plan only* — it never contains page content. Re-run after
 * editing the data file:
 *
 *   node scripts/content/generate-page-specs.mjs            # write specs
 *   node scripts/content/generate-page-specs.mjs --check    # CI: fail if stale
 *
 * Scope: production infrastructure for Mission 006. Does not generate content.
 */

import { mkdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { pages, routeToSlug, TEMPLATE_FILES } from './page-specs.data.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, '..', '..')
const SPECS_DIR = join(REPO_ROOT, 'docs', 'page-specs')

const CHECK_MODE = process.argv.includes('--check')

const TEMPLATE_LABEL = {
  commercial: 'Commercial page template',
  guide: 'Guide template',
  comparison: 'Comparison page template',
  herb: 'Herb authority template',
  compound: 'Compound authority template',
}

const PHASE_LABEL = {
  1: 'Phase 1 — Money pages (Days 1–30)',
  2: 'Phase 2 — High-intent guide stubs (Days 31–60)',
  3: 'Phase 3 — Hubs, comparisons & authority (Days 61–90)',
}

const bullets = (items) =>
  items.length ? items.map((i) => `- ${i}`).join('\n') : '- _None specified — confirm during expansion._'

const links = (items) =>
  items.length
    ? items.map((i) => (i.startsWith('/') ? `- [\`${i}\`](${i})` : `- ${i}`)).join('\n')
    : '- _None specified._'

function specMarkdown(p) {
  const slug = routeToSlug(p.route)
  const templatePath = `../${TEMPLATE_FILES[p.template]}`
  return `<!-- GENERATED by scripts/content/generate-page-specs.mjs — do not edit by hand.
     Edit scripts/content/page-specs.data.mjs and re-run the generator. -->

# Page Spec — \`${p.route}\`

> **Roadmap rank:** #${p.num} · **${PHASE_LABEL[p.phase]}**
> **Template:** [${TEMPLATE_LABEL[p.template]}](${templatePath})
> Plan only — this file specifies expansion work; it does **not** contain content.
> Source data: [\`scripts/content/page-specs.data.mjs\`](../../scripts/content/page-specs.data.mjs) · Playbook: [\`content-expansion-playbook.md\`](../content-expansion-playbook.md)

## Current state

- **Weighted score:** ${p.currentScore}
- **Body words:** ~${p.currentWords.toLocaleString()}
- **Signals:** ${p.currentState}

## Target state

- **Weighted score:** ${p.targetScore} (Δ +${p.targetScore - p.currentScore})
- **Body words:** ~${p.targetWords.toLocaleString()} (Δ +${(p.targetWords - p.currentWords).toLocaleString()})
- **Template to apply:** ${TEMPLATE_LABEL[p.template]}

## Missing sections

${bullets(p.sections)}

## Citation opportunities

${bullets(p.citations)}

## FAQ opportunities

${bullets(p.faq)}

## Schema opportunities

${bullets(p.schema)}

## Affiliate opportunities

> Always use \`AFFILIATE_TAGS.amazon\` (\`config/affiliate.ts\`) via \`RecommendedProduct\` / \`AffiliateProductCard\`. Never hardcode tags.

${bullets(p.affiliate)}

## Internal links

> Funnel toward money pages (#1–#9). Lists are directional, not exhaustive.

${links(p.internalLinks)}

## Evidence opportunities

${bullets(p.evidence)}
${
  p.ymyl
    ? `\n## ⚠️ YMYL caution\n\n- ${p.ymyl}\n`
    : ''
}
## Expansion checklist

Copy into the tracking issue/PR (see [\`templates/expansion-checklist.md\`](../templates/expansion-checklist.md)):

- [ ] Introduction
- [ ] How it works
- [ ] Evidence summary
- [ ] Dosing
- [ ] Safety
- [ ] FAQ
- [ ] Internal links
- [ ] Affiliate placements
- [ ] Schema
- [ ] References
`
}

function indexMarkdown() {
  const rows = pages
    .map(
      (p) =>
        `| #${p.num} | ${p.phase} | [\`${p.route}\`](./${routeToSlug(p.route)}.md) | ${TEMPLATE_LABEL[p.template]} | ${p.currentScore} → ${p.targetScore} | ~${p.currentWords.toLocaleString()} → ~${p.targetWords.toLocaleString()} |`,
    )
    .join('\n')
  return `<!-- GENERATED by scripts/content/generate-page-specs.mjs — do not edit by hand. -->

# Top 25 Page Specs — Index

One specification file per Top-25 expansion target. Each spec is a **plan only**
(current state, target state, missing sections, and per-page opportunities for
citations, FAQ, schema, affiliate, internal links, and evidence).

- **Engine / how to use:** [\`../content-expansion-playbook.md\`](../content-expansion-playbook.md)
- **Templates:** [\`../templates/\`](../templates/)
- **Checklist:** [\`../templates/expansion-checklist.md\`](../templates/expansion-checklist.md)
- **Source data:** [\`../../scripts/content/page-specs.data.mjs\`](../../scripts/content/page-specs.data.mjs)
- **Regenerate:** \`node scripts/content/generate-page-specs.mjs\`

| # | Phase | Route | Template | Score | Words |
|--:|:-----:|-------|----------|:-----:|:-----:|
${rows}

_Total: ${pages.length} specs. This is infrastructure, not content._
`
}

function main() {
  mkdirSync(SPECS_DIR, { recursive: true })

  const outputs = new Map()
  for (const p of pages) {
    outputs.set(join(SPECS_DIR, `${routeToSlug(p.route)}.md`), specMarkdown(p))
  }
  outputs.set(join(SPECS_DIR, 'README.md'), indexMarkdown())

  if (CHECK_MODE) {
    let stale = false
    for (const [file, content] of outputs) {
      const current = existsSync(file) ? readFileSync(file, 'utf8') : null
      if (current !== content) {
        stale = true
        console.error(`✗ stale: ${file.replace(REPO_ROOT + '/', '')}`)
      }
    }
    if (stale) {
      console.error('\nPage specs are stale. Run: node scripts/content/generate-page-specs.mjs')
      process.exit(1)
    }
    console.log(`✓ ${outputs.size} page specs up to date.`)
    return
  }

  for (const [file, content] of outputs) {
    writeFileSync(file, content, 'utf8')
  }
  console.log(`✓ wrote ${outputs.size} files to docs/page-specs/`)
}

main()
